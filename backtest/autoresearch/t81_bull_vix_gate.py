def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--date", default="2026-05-14")
    args = parser.parse_args()

    if not DIAG_FILE.exists():
        print(f"ERROR: {DIAG_FILE} not found", file=sys.stderr)
        sys.exit(1)

    result = analyze_date(args.date)
    if "error" in result:
        print(f"ERROR: {result['error']}", file=sys.stderr)
        sys.exit(1)

    # Print summary
    print(f"T81 Analysis for {args.date}:")
    print(f"  Ticks: {result['n_ticks']} ({result['n_real_bars']} real, {result['n_inprogress_bars']} in-progress)")
