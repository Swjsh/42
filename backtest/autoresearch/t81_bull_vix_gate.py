"""T81: BULL VIX gate diagnostic — why all BULL signals were silent on 2026-05-14.

Investigates: watcher-live-diag.jsonl 5/14 entries.
Computes: per-tick vix_direction using production deadband logic.
Finds: how many ticks would have passed Filter 8 (VIX < 17.20 OR vix_falling).

Usage:
    python -m autoresearch.t81_bull_vix_gate [--date YYYY-MM-DD]

Output:
    markdown/research/T81-BULL-VIX-GATE.md
"""
from __future__ import annotations

import argparse
import json
import pathlib
import sys
from datetime import datetime

REPO = pathlib.Path(__file__).resolve().parents[2]
DIAG_FILE = REPO / "automation" / "state" / "watcher-live-diag.jsonl"
OUT_MD = REPO / "markdown" / "research" / "T81-BULL-VIX-GATE.md"

VIX_BULL_LOW_THRESHOLD = 17.20  # mirror production filters.py
VIX_RISING_DEADBAND = 0.05       # same
LOOKBACK_BARS = 3                # proposed multi-bar lookback for "falling" check


def vix_direction_single(now: float, prior: float) -> str:
    """Production logic: single-bar comparison with 0.05 deadband."""
    if now > prior + VIX_RISING_DEADBAND:
        return "rising"
    if now < prior - VIX_RISING_DEADBAND:
        return "falling"
    return "flat"


def vix_direction_multi(now: float, prior_3bar: float) -> str:
    """Proposed: 3-bar lookback (15-minute prior). Larger moves detectable."""
    if now > prior_3bar + VIX_RISING_DEADBAND:
        return "rising"
    if now < prior_3bar - VIX_RISING_DEADBAND:
        return "falling"
    return "flat"


def analyze_date(target_date: str) -> dict:
    entries = []
    with DIAG_FILE.open() as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            try:
                d = json.loads(line)
            except Exception:
                continue
            fire_at = d.get("fire_at", "")
            if fire_at.startswith(target_date):
                d["_fire_at_dt"] = datetime.fromisoformat(fire_at)
                entries.append(d)

    if not entries:
        return {"error": f"No diag entries found for {target_date}"}

    entries.sort(key=lambda x: x["_fire_at_dt"])

    # Filter to entries that have real VIX data
    vix_values = [e["vix_now"] for e in entries]

    # Per-tick analysis
    rows = []
    for i, entry in enumerate(entries):
        vix_now = entry["vix_now"]
        bar_ts = entry.get("latest_bar_ts", "")
        bar_v = entry.get("latest_bar_v", -1)
        bar_time = bar_ts[11:16] if len(bar_ts) >= 16 else "?"

        # Single-bar prior
        vix_prior_single = vix_values[i - 1] if i > 0 else vix_now
        dir_single = vix_direction_single(vix_now, vix_prior_single)
        f8_single = (vix_now < VIX_BULL_LOW_THRESHOLD) or (dir_single == "falling")

        # Multi-bar prior (3 bars back)
        vix_prior_multi = vix_values[max(0, i - LOOKBACK_BARS)]
        dir_multi = vix_direction_multi(vix_now, vix_prior_multi)
        f8_multi = (vix_now < VIX_BULL_LOW_THRESHOLD) or (dir_multi == "falling")

        rows.append({
            "bar_time": bar_time,
            "bar_v": bar_v,
            "vix_now": round(vix_now, 3),
            "vix_prior_single": round(vix_prior_single, 3),
            "dir_single": dir_single,
            "f8_single": f8_single,
            "vix_prior_multi": round(vix_prior_multi, 3),
            "dir_multi": dir_multi,
            "f8_multi": f8_multi,
        })

    # Stats
    n = len(rows)
    n_f8_single = sum(1 for r in rows if r["f8_single"])
    n_f8_multi = sum(1 for r in rows if r["f8_multi"])
    vix_min = min(vix_values)
    vix_max = max(vix_values)
    vix_mean = sum(vix_values) / len(vix_values)
    n_real_bars = sum(1 for e in entries if e.get("latest_bar_v", 0) > 0)
    n_inprog_bars = n - n_real_bars

    # Bars where single fails but multi passes
    n_single_fail_multi_pass = sum(
        1 for r in rows if not r["f8_single"] and r["f8_multi"]
    )

    return {
        "date": target_date,
        "n_ticks": n,
        "n_real_bars": n_real_bars,
        "n_inprogress_bars": n_inprog_bars,
        "vix_min": round(vix_min, 3),
        "vix_max": round(vix_max, 3),
        "vix_mean": round(vix_mean, 3),
        "vix_threshold": VIX_BULL_LOW_THRESHOLD,
        "deadband": VIX_RISING_DEADBAND,
        "f8_passes_single_bar": n_f8_single,
        "f8_passes_multi_bar_3": n_f8_multi,
        "single_fail_multi_pass": n_single_fail_multi_pass,
        "f8_pass_rate_single": round(n_f8_single / n, 3),
        "f8_pass_rate_multi": round(n_f8_multi / n, 3),
        "rows": rows,
    }


